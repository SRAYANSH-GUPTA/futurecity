function buttonenter(b)
{
  document.querySelector(b).style.backgroundColor = "white";
  document.querySelector(b).style.height = "'50px";
  document.querySelector(b).style.width = "50px";
}
function buttonleave(b)
{
  document.querySelector(b).style.backgroundColor = "orange";
  document.querySelector(b).style.height = "30px";
  document.querySelector(b).style.width = "30px";
}
  
