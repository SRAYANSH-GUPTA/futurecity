function buttonenter(b)
{
  var ele = document.querySelector(b);
  ele.style.backgroundColor = "white";
  ele.style.height = "70rem";
  ele.style.width = "160rem";
}
function buttonleave(b)
{
  var elem = document.querySelector(b);
  elem.style.backgroundColor = "orange";
  elem.style.height = "62.4rem";
  elem.style.width = "151.9rem";
}
  
